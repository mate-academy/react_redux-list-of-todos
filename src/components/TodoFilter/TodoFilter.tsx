import React, { ChangeEvent } from 'react';
import { useAppDispatch, useAppSelector } from '../../app/hooks';
import { filterSlice } from '../../features/filter';
import { Status } from '../../types/Status';

export const TodoFilter: React.FC = () => {
  const dispatch = useAppDispatch();
  const { query, status } = useAppSelector(state => state.filter);

  function handleChangeQuery(newQuery: string) {
    const normalizedQuery = newQuery.toLowerCase();

    if (normalizedQuery !== query) {
      dispatch(filterSlice.actions.changeQuery(normalizedQuery));
    }
  }

  function handleChangeStatus(event: ChangeEvent<HTMLSelectElement>) {
    if (status !== event.currentTarget.value) {
      dispatch(
        filterSlice.actions.changeStatus(event.currentTarget.value as Status),
      );
    }
  }

  return (
    <form
      className="field has-addons"
      onSubmit={event => event.preventDefault()}
    >
      <p className="control">
        <span className="select">
          <select data-cy="statusSelect" onChange={handleChangeStatus}>
            <option value="all">All</option>
            <option value="active">Active</option>
            <option value="completed">Completed</option>
          </select>
        </span>
      </p>

      <p className="control is-expanded has-icons-left has-icons-right">
        <input
          data-cy="searchInput"
          type="text"
          className="input"
          placeholder="Search..."
          value={query}
          onChange={event => handleChangeQuery(event.target.value)}
        />
        <span className="icon is-left">
          <i className="fas fa-magnifying-glass" />
        </span>

        <span className="icon is-right" style={{ pointerEvents: 'all' }}>
          {query && (
            <button
              data-cy="clearSearchButton"
              type="button"
              className="delete"
              onClick={() => handleChangeQuery('')}
            />
          )}
        </span>
      </p>
    </form>
  );
};
